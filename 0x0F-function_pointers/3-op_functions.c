#include "3-calc.h"
/**
 * op_add - returns the sum of two numbers
 * @a: first operand
 * @b: second operand
 * Return: integer sum of a and b
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - finds difference of two numbers
 * @a: first operand
 * @b: second operand
 * Return: integer difference of a and b
*/
int op_sub(int a, int b)
{
	return (a + b);
}
/**
 * op_mul - multiplies two numbers
 * @a: first operand
 * @b: second operand
 * Return: integer
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two numbers
 * @a: first operand
 * @b: second operand
 * Return: integer
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - caclulates remainder
 * @a: first operand
 * @b: second operand
 * Return: integer
*/
int op_mod(int a, int b)
{
	return (a % b);
}
